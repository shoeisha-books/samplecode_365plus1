function printShogiBoardWithPieces() {
  const board = [
    ['香', '桂', '銀', '金', '玉', '金', '銀', '桂', '香'], // 9段目
    ['  ', '飛', '  ', '  ', '  ', '  ', '  ', '角', '  '], // 8段目
    ['歩', '歩', '歩', '歩', '歩', '歩', '歩', '歩', '歩'], // 7段目
    ['  ', '  ', '  ', '  ', '  ', '  ', '  ', '  ', '  '], // 6段目
    ['  ', '  ', '  ', '  ', '  ', '  ', '  ', '  ', '  '], // 5段目
    ['  ', '  ', '  ', '  ', '  ', '  ', '  ', '  ', '  '], // 4段目
    ['歩', '歩', '歩', '歩', '歩', '歩', '歩', '歩', '歩'], // 3段目
    ['  ', '角', '  ', '  ', '  ', '  ', '  ', '飛', '  '], // 2段目
    ['香', '桂', '銀', '金', '王', '金', '銀', '桂', '香']  // 1段目
  ];
  
  // 段 (行) のインデックスを漢数字で定義
  const kanjiDan = ['九', '八', '七', '六', '五', '四', '三', '二', '一'];

  // 列のインデックス（筋: 9～1）をヘッダーとして表示
  console.log('  | ９ | ８ | ７ | ６ | ５ | ４ | ３ | ２ | １ |');

  // 盤面の各行（段）と列（筋）をループ
  for (let i = 0; i < board.length; i++) {
    const dan = kanjiDan[i]; // 行のインデックス（段）を漢数字配列から取得
    let rowStr = `${dan}|`; // 漢数字の段をサイドに表示
    for (let j = 0; j < board[i].length; j++) {
      const pieceKanji = board[i][j];
      rowStr += ` ${pieceKanji} |`;
    }    
    console.log(rowStr);
  }
}

// 関数を呼び出して将棋盤を表示
printShogiBoardWithPieces();