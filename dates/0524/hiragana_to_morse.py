HIRAGANA_TO_MORSE = {
    'あ': '－－・－－',     'い': '・－',     'う': '・・ー',   'え': '－・－－－',     'お': '・－・・・',
    'か': '・－・・',   'き': '－・－・・',   'く': '・・・ー',   'け': '－・－－',     'こ': 'ーーーー',
    'さ': '－・－・－',   'し': '－－・－・',     'す': '－－－・－',     'せ': '・－－－・',   'そ': '－－－・',
    'た': '－・',     'ち': '・・ー・',   'つ': '・－－・',     'て': '・－・－－',   'と': '・・－・・',
    'な': '・－・',   'に': '－・－・',     'ぬ': '・・・・',   'ね': '－－・－',   'の': '・・－－',
    'は': '－・・・',   'ひ': '－－・・－',   'ふ': '－－・・',   'へ': '・',     'ほ': '－・・',
    'ま': '－・・－',   'み': '・・－・－',   'む': '－',     'め': '－・・・－',   'も': '－・・－・',
    'や': '・－－',     'ゆ': '－・・－－',   'よ': '－－',
    'ら': '・・・',   'り': '－－・',     'る': '－・－－・',     'れ': '－－－',     'ろ': '・－・－',
    'わ': '－・－',   'を': '・－－－',   'ん': '・－・－・',
    '゛': '・・', '゜': '・・－－・', 'ー': '・－－・－'
}

def to_morse_code(hiragana_text):
    morse_code_result = []
    for char in hiragana_text:
        if char in HIRAGANA_TO_MORSE:
            morse = HIRAGANA_TO_MORSE[char]
            morse_code_result.append(morse + " ")
        else:
            print(f"警告: 文字 '{char}' に対応するモールス信号が定義されていません。スキップします。")
            continue
    return ''.join(morse_code_result)

if __name__ == "__main__":
    hiragana_input = "きようはもーるすしんこ゛うのひ"
    morse_output = to_morse_code(hiragana_input)
    print(f"モールス信号: {morse_output}")
